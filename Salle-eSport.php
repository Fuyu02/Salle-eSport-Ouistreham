            <p><h3 id="esport">Qu'est ce que l'esport?</h3></br></p>
        
            <p>Le e-sport désigne un sport électronique. C’est la pratique, en compétition, d’un jeu vidéo, seul ou en équipe, sur console de jeux ou ordinateur, </br>
            en réseau local ou via internet. L’e-sport s’apparente au sport par différents aspects : l’affrontement, la compétition, les entraînements, </br>
            l’organisation de tournois.</br></p>
            </br>
            <p>C’est la première salle publique du genre dans le département, financée à hauteur de 35 000 €, à 80% par la région Normandie et à 20% par la commune.</br>
            Pour la ville, il s’agit d’abord de vulgariser, développer, la pratique de ce sport cérébral, et de favoriser une pratique raisonnée et un savoir être, </br>
            à l’opposé de pratiques addictives. Il s’agit aussi, pour la région Normandie, de faire émerger des talents, organiser la filière e-sportive, </br>
            qui pourrait bientôt devenir une discipline olympique puisqu’il sera en préfiguration aux JO de Paris 2024. </br>
            Cela répond aussi à une professionnalisation qui se développe de façon galopante dans le domaine du game.</br></p>
            </br>
            <p>La salle est équipée de dix ordinateurs dont l’enveloppe est transparente et permet de voir dedans. Quand toutes les unités centrales sont allumées, </br>
            des LED de couleur illuminent l’intérieur et nous plongent aussitôt dans une ambiance gaming, dans l’univers du e-sport. </br>
            La configuration de l’espace permet à deux équipes de cinq joueurs de s’affronter en tournoi, sur le même jeu.</br></p>
            </br>
            <img src="img2.jpg" id="imgPresentation">
            </br>
            </br>
            <p>Ce sont, bien sûr, en tout premier lieu, les ados, biberonnés à la dimension numérique. Mais, cela continue d’intéresser de très nombreux adultes, </br>
            jusqu’aux quadras et quinquas, qui ont aussi grandi avec l’évolution des jeux numériques et du e-sport.</br>
            Pour l’instant, compte tenu des souhaits des pratiquants, le Pavillon a choisi trois jeux : Overwatch, Hearthstone, Paladin, et Fortnite. </br>
            En fonction de la demande, la proposition évoluera vers de nouveaux jeux et types de jeux.</br></p>
            </br>
            <p>À terme, c’est une salle de 40 m² qui accueillera cet équipement complété par deux consoles de jeux vidéo permettant à huit joueurs de jouer. </br>
            L’espace permettra aussi d’accueillir des spectateurs, et favorisera l’émulation, l’échange et la sociabilisation entre les pratiquants.</br>
            Et d'autres jeux seront rajoutés au fur et à mesure.</br></p>
